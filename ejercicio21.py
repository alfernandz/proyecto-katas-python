#Crea una función que calcule el cubo de un número dado mediante una función lambda.

#Creamos la función expresando la operación matemática
funcion_cubo = lambda x: x*x*x
#Guardamos la función en una variable dándole como parámetro el número con el que queremos operar (la x de la función)
resultado = funcion_cubo (3)
#Imprimimos la variable para comprobar que la función es correcta
print (resultado)

